import 'package:flutter/material.dart';

class TerminalTheme {
  final String name;
  final int background;
  final int foreground;
  final int accent;
  final int cursor;
  final int selection;
  final Map<int, int> ansiColors;

  const TerminalTheme({
    required this.name,
    required this.background,
    required this.foreground,
    required this.accent,
    this.cursor = 0xFFFFFFFF,
    this.selection = 0xFF000000,
    this.ansiColors = const {},
  });

  Color get backgroundColor => Color(background);
  Color get foregroundColor => Color(foreground);
  Color get accentColor => Color(accent);
  Color get cursorColor => Color(cursor);
  Color get selectionColor => Color(selection);

  Color getAnsiColor(int index) {
    return Color(ansiColors[index] ?? _defaultAnsiColors[index] ?? 0xFF000000);
  }
}

